export const getAmenityIcon = (amenityName: string) => {
  const name = amenityName.toLowerCase().trim();

  // Pemetaan kata kunci ke ikon
  if (name.includes("wifi") || name.includes("internet")) return Wifi;
  if (name.includes("ac") || name.includes("pendingin")) return WindIcon;
  if (name.includes("listrik") || name.includes("daya")) return Zap;
  if (name.includes("parkir") || name.includes("garasi")) return Car;
  if (name.includes("kasur") || name.includes("tempat tidur") || name.includes("bed")) return Bed;
  if (name.includes("lemari") || name.includes("wardrobe")) return Box;
  if (name.includes("meja") || name.includes("kursi") || name.includes("desk")) return Monitor; // Atau ikon lain yang cocok
  if (name.includes("tv") || name.includes("televisi")) return Tv;
  if (name.includes("dapur") || name.includes("dapur umum") || name.includes("kompor")) return Utensils;
  if (name.includes("kamar mandi") || name.includes("toilet") || name.includes("wc")) return Bath;
  if (name.includes("air") || name.includes("pdam")) return Droplets;
  if (name.includes("ruang tamu") || name.includes("sofa")) return Sofa;
  if (name.includes("kunci") || name.includes("akses 24 jam")) return Key;
  if (name.includes("mesin cuci") || name.includes("laundry")) return WashingMachine;
  if (name.includes("setrika")) return Shirt;
  if (name.includes("keamanan") || name.includes("cctv") || name.includes("satpam")) return ShieldCheck;

  // Ikon default jika tidak ada yang cocok
  return CheckCircle2;
};
